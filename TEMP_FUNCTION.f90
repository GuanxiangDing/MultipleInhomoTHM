
FUNCTION TEMP_I(TEMPN,Num,X,Z)
INTEGER TEMPN, Num
DOUBLE PRECISION TEMP_I,X,Z
DOUBLE PRECISION W11,W12,W21,W22,T1,T2

T1=Z*DLOG(X*X+Z*Z)+2.D0*X*DATAN(Z/X)
T2=X*DLOG(X*X+Z*Z)+2.D0*Z*DATAN(X/Z)

W11=T1; W12=T2
W21=T2; W22=T1
	
IF(TEMPN==1.AND.Num==1) GOTO 11
IF(TEMPN==1.AND.Num==2) GOTO 12

IF(TEMPN==2.AND.Num==1) GOTO 21
IF(TEMPN==2.AND.Num==2) GOTO 22


11  TEMP_I = W11
    RETURN
12  TEMP_I = W12
    RETURN
21  TEMP_I = W21
    RETURN
22  TEMP_I = W22
    RETURN	
	
END
	
	
